package it.betacom.milano.businesscomponent.model;

public class CorsoCorsista {
	private long codice_corso;
	private long codice_corsista;
	
	
	
	public CorsoCorsista() {}
	
	public long getCodice_corso() {
		return codice_corso;
	}
	public void setCodice_corso(long codice_corso) {
		this.codice_corso = codice_corso;
	}
	public long getCodice_corsista() {
		return codice_corsista;
	}
	public void setCodice_corsista(long codice_corsista) {
		this.codice_corsista = codice_corsista;
	}
	
	

}
